# Exercise: Create a variable, pi, that points to an approximation for the
# value of π.  Create a variable, r, for the radius of a circle that has a
# value of 10.  Calculate the area of the circle (π times the radius squared).

pi = 3.14
r = 10

area = pi * r ** 2
print('The area of a circle with r = 10 is', area);
